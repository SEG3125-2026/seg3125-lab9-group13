// Team image convention:
// 1. Create a folder for each game using its slug:
//    public/images/games/<slug>/
// 2. Add these files inside the folder:
//    cover.jpg
//    shot-1.jpg
//    shot-2.jpg
//    shot-3.jpg
// 3. Teammates only need to add/replace image files.
//    They do not need to change component code.

function buildImageSet(slug) {
  return {
    coverImage: `/images/games/${slug}/cover.jpg`,
    screenshots: [
      `/images/games/${slug}/shot-1.jpg`,
      `/images/games/${slug}/shot-2.jpg`,
      `/images/games/${slug}/shot-3.jpg`,
    ],
  }
}

